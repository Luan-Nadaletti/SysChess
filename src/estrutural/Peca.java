package estrutural;

public abstract class Peca {

    protected int linha, coluna;

    public abstract boolean validaMovimento();

    public int getLinha() {
        return linha;
    }

    public void setLinha(int linha) {
        this.linha = linha;
    }

    public int getColuna() {
        return coluna;
    }

    public void setColuna(int coluna) {
        this.coluna = coluna;
    }

}
